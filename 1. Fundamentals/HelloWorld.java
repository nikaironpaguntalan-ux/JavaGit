// Import statements (optional, depending on the program)

// Class declaration
public class HelloWorld{
    // Main method: the entry point of the program

    // public: The method is accessible from anywhere.
    // static: The method can be called without creating an object of the class.
    // void: The method doesn't return any value.
    // main: The name of the method that the JVM looks for when starting the program.
    // String[] args: This is an array that stores command-line arguments passed to the program.
    public static void main(String[] args){
        System.out.println("Hello World!");
    }
}